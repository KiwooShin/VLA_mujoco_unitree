# Validation against dataset GT
# ---------------------------------------------------------------------------
def validate_grounding(
    dataset_dir: str,
    n_frames:    int  = 500,
    verbose:     bool = True,
) -> dict:
    """
    Compare grounding output against privileged GT goal from a generated dataset.

    Parameters
    ----------
    dataset_dir : path produced by gen_dataset.py
    n_frames    : number of frames to sample for validation
    verbose     : print per-episode summary

    Returns
    -------
    dict: mean_dist_err, mean_bearing_err_deg, detection_rate, n_eval
    """
    import glob
    import json

    data_dir  = os.path.join(dataset_dir, "data", "chunk-000")
    meta_dir  = os.path.join(dataset_dir, "meta")
    video_dir = os.path.join(dataset_dir, "videos")

    parquet_files = sorted(glob.glob(os.path.join(data_dir, "episode_*.parquet")))
    if not parquet_files:
        raise FileNotFoundError(f"No parquet files found in {data_dir}")

    # Read tasks
    tasks = {}
    try:
        tasks_path = os.path.join(meta_dir, "tasks.jsonl")
        with open(tasks_path) as f:
            for line in f:
                t = json.loads(line)
                tasks[t["task_index"]] = t["task"]
    except Exception:
        pass

    intr = get_ego_intrinsics(EGO_W, EGO_H, EGO_FOVY)

    dist_errs:    list = []
    bearing_errs: list = []
    n_detected         = 0
    n_total            = 0

    for pf in parquet_files:
        import pandas as pd
        df = pd.read_parquet(pf)
        if df.empty:
            continue

        ep_idx     = int(df["episode_index"].iloc[0])
        task_idx   = int(df["task_index"].iloc[0])
        task_desc  = tasks.get(task_idx, "")

        # Parse colour and shape from instruction
        color, shape = _parse_instruction(task_desc)
        if color is None:
            continue

        # Load ego RGB video
        ego_mp4 = os.path.join(video_dir, f"episode_{ep_idx:06d}_ego.mp4")
        if not os.path.isfile(ego_mp4):
            continue

        # Read frames lazily (one at a time) to avoid MemoryError
        reader = imageio.get_reader(ego_mp4)
        try:
            n_video_frames = reader.count_frames()
        except Exception:
            n_video_frames = len(df)
        reader.close()

        n_ep_frames = min(len(df), n_video_frames)
        n_sample = max(1, n_frames // len(parquet_files))
        n_sample = min(n_sample, n_ep_frames)
        sample_indices = np.linspace(0, n_ep_frames - 1, n_sample, dtype=int)

        reader = imageio.get_reader(ego_mp4)
        for si in sample_indices:
            if si >= len(df) or si >= n_ep_frames:
                continue

            try:
                frame = reader.get_data(int(si))
            except Exception:
                continue

            row      = df.iloc[si]
            gt_goal  = np.array(row["goal"])  # [dist, cosθ, sinθ]
            gt_dist  = float(gt_goal[0])
            gt_bear  = math.atan2(float(gt_goal[2]), float(gt_goal[1]))

            ego_rgb = np.array(frame, dtype=np.uint8)

            # Use a flat synthetic depth map at GT distance (validation mode)
            ego_depth_synth = np.full((EGO_H, EGO_W), gt_dist, dtype=np.float32)

            result = ground(ego_rgb, ego_depth_synth, color, shape, intr)

            n_total += 1
            if not result.not_visible and result.confidence > MIN_CONFIDENCE:
                n_detected += 1
                dist_err   = abs(result.dist - gt_dist)
                pred_bear  = math.atan2(result.sin_th, result.cos_th)
                bear_err   = abs(math.atan2(
                    math.sin(pred_bear - gt_bear),
                    math.cos(pred_bear - gt_bear)
                ))
                dist_errs.append(dist_err)
                bearing_errs.append(math.degrees(bear_err))
        reader.close()

        if n_total >= n_frames:
            break

    detection_rate     = n_detected / max(1, n_total)
    mean_dist_err      = float(np.mean(dist_errs))    if dist_errs    else float("nan")
    mean_bearing_err   = float(np.mean(bearing_errs)) if bearing_errs else float("nan")

    if verbose:
        print(f"\n[grounding validation]")
        print(f"  frames evaluated : {n_total}")
        print(f"  detected         : {n_detected}  ({detection_rate:.2%})")
        print(f"  mean dist err    : {mean_dist_err:.3f} m")
        print(f"  mean bearing err : {mean_bearing_err:.2f} deg")

    return {
        "mean_dist_err":        mean_dist_err,
        "mean_bearing_err_deg": mean_bearing_err,
        "detection_rate":       detection_rate,
        "n_eval":               n_total,
    }


# ---------------------------------------------------------------------------
# Parse colour/shape from instruction string
# ---------------------------------------------------------------------------
def _parse_instruction(instruction: str) -> tuple[Optional[str], Optional[str]]:
    """Extract the first matching colour and shape from a text instruction.

    Uses whole-word matching to avoid false positives like 'red' matching
    inside 'purple-colored'.
    """
    import re
    from code.arena import COLORS, SHAPES  # noqa: already in path
    text = instruction.lower()
    color = next(
        (c[0] for c in COLORS
         if re.search(r'\b' + c[0] + r'\b', text)),
        None,
    )
    shape = next((s[0] for s in SHAPES if s[0] in text), None)
    return color, shape


