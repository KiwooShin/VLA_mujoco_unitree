"""
grounding.py — Classical grounding module for G1Nav (ADR Architecture A v2.0).

Given ego RGB + depth + target (color, shape) + camera intrinsics, produces:
  - egocentric goal (dist, cos θ, sin θ)
  - confidence score  [0, 1]
  - not_visible flag

Pipeline
--------
1. HSV colour threshold with per-colour bounds → binary mask
2. Shape/blob filter (min area, circularity) → best candidate blob
3. Morphological erosion to remove noisy border pixels
4. Median depth over eroded mask → back-project to 3-D camera-frame point
5. Rotate from camera frame to egocentric robot frame → (dist, yaw_err)

Validation (standalone)
-----------------------
Run against frames generated by gen_dataset.py; compare (dist, bearing) vs
the privileged GT goal from the parquet `goal` column.

API
---
ground(ego_rgb, ego_depth, target_color, target_shape, intrinsics, robot_yaw=None)
    -> GroundingResult(dist, cos_th, sin_th, confidence, not_visible)

validate_grounding(dataset_dir, n_frames=500)
    -> dict with mean_dist_err, mean_bearing_err_deg, detection_rate
"""

