# Smoke test
# ---------------------------------------------------------------------------
def _smoke_test() -> None:
    """Synthetic red-ball detection smoke test (also runnable as `python
    code/grounding.py` via the old-path CLI shim, or `python -m
    code.perception.grounding`)."""
    from code.arena import EGO_FOVY, EGO_H, EGO_W, get_ego_intrinsics

    print("grounding.py smoke test...")

    # Synthetic test: render a red ball in a blank image and try to detect it
    h, w = EGO_H, EGO_W
    img = np.zeros((h, w, 3), dtype=np.uint8)
    img[:, :] = [30, 30, 30]   # dark background

    # Draw a red circle in upper-right quadrant
    cx_pix, cy_pix = w * 3 // 4, h // 3
    cv2.circle(img, (cx_pix, cy_pix), 35, (220, 40, 40), -1)   # RGB red

    depth = np.full((h, w), 3.0, dtype=np.float32)  # flat at 3m
    intr  = get_ego_intrinsics(w, h, EGO_FOVY)

    r = ground(img, depth, "red", "ball", intr, return_mask=True)
    print(f"  detected={not r.not_visible}  dist={r.dist:.2f}m  "
          f"yaw_err={math.degrees(math.atan2(r.sin_th,r.cos_th)):.1f}°  "
          f"conf={r.confidence:.2f}")

    # Blue cube — should not detect (no blue in image)
    r2 = ground(img, depth, "blue", "cube", intr)
    print(f"  blue (not present) detected={not r2.not_visible}  (expected False)")

    assert not r.not_visible, "Failed to detect red ball in synthetic image"
    assert r2.not_visible,    "False positive for blue cube"

    print("grounding.py smoke PASS")


if __name__ == "__main__":
    import sys
    _smoke_test()
    sys.exit(0)
