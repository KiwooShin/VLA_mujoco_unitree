"""
code/perception/grounding.py — ground() dispatch (RF-1 split of
code/grounding.py; docs/refactor_plan.md).

Given ego RGB + depth + target (color, shape) + camera intrinsics, produces:
  - egocentric goal (dist, cos θ, sin θ)
  - confidence score  [0, 1]
  - not_visible flag

`ground()` dispatches between the learned GROUND_NET backend
(code/perception/ground_net.py + code/perception/detector/) and the classical
HSV+depth pipeline (code/perception/hsv_pipeline.py), with a graceful
classical fallback when the GROUND_NET checkpoint is unavailable
(docs/nx9_avoid.md).

This module OWNS all of this pipeline's mutable state (docs/refactor_plan.md
invariant 5): the GROUND_NET* env toggles (read once at import) and the
single `GroundNetState` singleton (detector cache, NX-7 track state,
one-shot notice flags, VF-1 heatmap cache, latency log) live here and only
here. The old top-level `code/grounding.py` is a sys.modules alias to this
exact module object, so old-path callers (`code.grounding.ground`,
`code.grounding.GROUND_NET`, `code.grounding.get_ground_net_last_heatmap()`,
monkeypatching `code.grounding.ground = ...`, etc.) observe precisely the
same state this module mutates -- no copying, no drift.

Validation (standalone)
-----------------------
Run against frames generated by gen_dataset.py; compare (dist, bearing) vs
the privileged GT goal from the parquet `goal` column.

API
---
ground(ego_rgb, ego_depth, target_color, target_shape, intrinsics, robot_yaw=None)
    -> GroundingResult(dist, cos_th, sin_th, confidence, not_visible)

validate_grounding(dataset_dir, n_frames=500)
    -> dict with mean_dist_err, mean_bearing_err_deg, detection_rate
"""
